"""## Function to get socio-demographic bins of a census and twitter"""

def get_census_bin_counts(census_data, cols):
    population = census_data['population']
    bin_counts = np.sum(census_data[cols].to_numpy() * np.tile(population, (len(cols), 1)).T  / 100, axis=0)
    return bin_counts


def get_twitter_bin_counts(user_demographics, twitter_bins):
    if len(user_demographics) == 0:
        return np.zeros(len(twitter_bins) - 1)

    bin_counts = []
    for bin1, bin2 in zip(twitter_bins, twitter_bins[1:]):
        bin_counts.append(np.sum(user_demographics.between(bin1, bin2, inclusive='left')))

    return np.array(bin_counts)

