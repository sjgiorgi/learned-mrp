def bin_percents(x, bins):
    percents = torch.zeros(len(bins) - 1, device=DEVICE)

    for i in range(len(percents)):
        bin1 = bins[i]
        bin2 = bins[i + 1]
        size = bin2 - bin1
        # mask = ((x >= bin1) & (x < bin2)).float()
        mask = F.relu(x - bin1) * F.relu(bin2 - x)
        masked = x * mask
        percents[i] += torch.sum(torch.abs(masked / (torch.abs(masked) + 1e-3)))

    return percents / percents.sum()


def pearsonr_loss(y_pred, y_true):
    vx = y_pred - torch.mean(y_pred)
    vy = y_true - torch.mean(y_true)
    loss = torch.sum(vx * vy) / (torch.sqrt(torch.sum(vx ** 2)) * torch.sqrt(torch.sum(vy ** 2)))
    return -loss


def train(model, X, y, optimizer, loss_type='pearsonr'):
    model.train()
    optimizer.zero_grad()
    y_tensor = torch.tensor(y, dtype=torch.float32, device=DEVICE) # (N)

    # out = model(X) # (N)
    out = model(X)
    if loss_type == 'mse':
        loss = F.mse_loss(out, y_tensor)
    elif loss_type == 'pearsonr':
        loss = pearsonr_loss(out, y_tensor)
    elif loss_type == 'custom':
        loss1 = pearsonr_loss(out, y_tensor)
        loss2 = torch.sum(torch.log(out / y_tensor))
        loss = loss1 + loss2

    loss.backward()
    optimizer.step()

    y_pred = out.cpu().detach().numpy()
    r2 = r2_score(y_pred, y)
    r = pearsonr(y_pred, y)
    mse = mean_squared_error(y_pred, y)

    return r, r2, mse


def test(model, X, y):
    model.eval()
    y_tensor = torch.tensor(y, dtype=torch.float32, device=DEVICE)

    with torch.no_grad():
        out = model(X)

    y_pred = out.cpu().numpy()
    r2 = r2_score(y_pred, y)
    r = pearsonr(y_pred, y)
    mse = mean_squared_error(y_pred, y)

    return r, r2, mse

class PS_NN(nn.Module):
    def __init__(self, num_factors=25, num_dem=4, use_smoothing=None, combine='multi-hot', distance='l1'):
        super().__init__()
        # parameters
        self.num_factors = num_factors
        self.num_dem = num_dem
        self.use_smoothing = use_smoothing.lower() if use_smoothing else ''
        self.combine = combine.lower() if combine else ''
        self.distance = distance.lower() if distance else ''

        if self.use_smoothing == 'constant':
            self.smoothing = torch.tensor([10.], device=DEVICE)
        elif self.use_smoothing == 'single':
            self.smoothing = nn.Parameter(torch.tensor([10.], requires_grad=True)).to(DEVICE)
            self.smoothing2 = nn.Parameter(torch.tensor([10.], requires_grad=True)).to(DEVICE)
        elif self.use_smoothing == 'multiple':
            self.smoothing = nn.Parameter(torch.tensor([10.] * self.num_dem, requires_grad=True)).to(DEVICE)
            self.smoothing2 = nn.Parameter(torch.tensor([10.] * self.num_dem, requires_grad=True)).to(DEVICE)
        else:
            self.smoothing = torch.zeros(1, device=DEVICE)

        if self.combine not in ('multi-hot', 'one-hot', 'both'):
            raise ValueError(f'invalid combine: {combine}')

        # weights
        # self.hidden1 = nn.Linear(num_factors, num_factors)
        # self.hidden2 = nn.Linear(num_factors, num_factors)
        self.fc1 = nn.Linear(num_factors, 1)

    def forward(self, x, return_weights=False):
        # x is a list of tuples containing user_ids, user_scores, census_bin_counts, twitter_bin_counts[0:f], user_embeddings for all counties
        # |x| = number of counties N
        # x[j] = (user_ids[0:u], user_scores[0:u], census_bin_counts[0:f], twitter_bin_counts[0:f], user_embeddings[0:u, 0:f])
        # |x[j]| = number of users M_N in county j
        county_weighted_avgs = []
        all_user_ids = []
        all_user_weights = []
        all_kls = []
        user_counts = []

        for data in tqdm(x):
            user_scores = data[1] # (u)
            census_bin_counts = data[2] # [(11), (10), (2), (2)]
            twitter_bin_counts = data[3] # [(11), (10), (2), (2)]
            user_dems = data[4] # [(u, 11), (u, 10), (u, 2), (u, 2)]

            # convert to tensors
            user_scores = torch.tensor(user_scores, dtype=torch.float32, device=DEVICE) # (u)
            census_bin_percents = [torch.tensor(x / np.sum(x), dtype=torch.float32, device=DEVICE) for x in census_bin_counts] # [(11), (10), (2), (2)]
            twitter_bin_counts = [torch.tensor(x, dtype=torch.float32, device=DEVICE) for x in twitter_bin_counts] # [(11), (10), (2), (2)]
            twitter_bin_percents = [0] * self.num_dem

            # informed smoothing
            for i in range(self.num_dem):
                if self.use_smoothing == 'multiple':
                    smoothing = self.smoothing[i]
                    smoothing2 = self.smoothing2[i]
                    twitter_bin_percents[i] = (twitter_bin_counts[i] + smoothing * census_bin_percents[i]) / torch.sum(twitter_bin_counts[i] + smoothing) # (num_factors)
                    twitter_bin_percents[i] = torch.sigmoid(smoothing2 * twitter_bin_percents[i])
                elif self.use_smoothing in ('constant', 'single'):
                    smoothing = self.smoothing
                    smoothing2 = self.smoothing2
                    twitter_bin_percents[i] = (twitter_bin_counts[i] + smoothing * census_bin_percents[i]) / torch.sum(twitter_bin_counts[i] + smoothing) # (num_factors)
                    twitter_bin_percents[i] = torch.sigmoid(smoothing2 * twitter_bin_percents[i])
                else:
                    twitter_bin_percents[i] = twitter_bin_counts[i] / torch.sum(twitter_bin_counts[i]) # (num_factors)

            # combine
            if self.combine == 'multi-hot':
                census_encoding = percentage_multi_hot_encoding(census_bin_percents) # (num_factors)
                twitter_encoding = percentage_multi_hot_encoding(twitter_bin_percents) # (num_factors)
                user_encodings = embeddings_multi_hot_encoding(user_dems) # (u, num_factors)
            elif self.combine == 'one-hot':
                census_encoding = percentage_one_hot_encoding(census_bin_percents) # (num_factors)
                twitter_encoding = percentage_one_hot_encoding(twitter_bin_percents) # (num_factors)
                user_encodings = embeddings_one_hot_encoding(user_dems) # (u, num_factors)
            elif self.combine == 'both':
                census_encoding1 = percentage_multi_hot_encoding(census_bin_percents)
                census_encoding2 = percentage_multi_hot_encoding(census_bin_percents)
                census_encoding = torch.concat([census_encoding1, census_encoding2], axis=0) # (num_factors)

                twitter_encoding1 = percentage_one_hot_encoding(twitter_bin_percents)
                twitter_encoding2 = percentage_one_hot_encoding(twitter_bin_percents)
                twitter_encoding = torch.concat([twitter_encoding1, twitter_encoding2], axis=0) # (num_factors)

                user_encodings1 = embeddings_multi_hot_encoding(user_dems)
                user_encodings2 = embeddings_one_hot_encoding(user_dems)
                user_encodings = np.concatenate([user_encodings1, user_encodings2], axis=1) # (u, num_factors)

            # convert to tensor
            user_encodings = torch.tensor(user_encodings, dtype=torch.float32, device=DEVICE) # (u, num_factors)

            # distance metric
            if self.distance == 'l1':
                user_features = torch.abs(twitter_encoding - census_encoding) * user_encodings # (u, num_factors)
            elif self.distance == 'l2':
                user_features = torch.square(twitter_encoding - census_encoding) * user_encodings # (u, num_factors)
            else:
                user_features = (twitter_encoding - census_encoding) * user_encodings # (u, num_factors)

            # user_features = self.hidden1(user_features) # (u, num_factors)
            # user_features = F.relu(user_features)
            # user_features = self.hidden2(user_features) # (u, num_factors)
            # user_features = F.relu(user_features)
            user_features = self.fc1(user_features) # (u, 1)
            user_weights = F.softmax(user_features, dim=0) * len(user_features) # (u, 1)

            if return_weights:
                all_user_ids.append(data[0])
                all_user_weights.append(user_weights.detach().squeeze().cpu().numpy())

            county_weighted_avg = torch.sum(user_weights.squeeze() * user_scores) / torch.sum(user_weights) # (1)
            county_weighted_avgs.append(county_weighted_avg)

        county_weighted_avgs = torch.stack(county_weighted_avgs)
        if return_weights:
            all_user_ids = np.concatenate(all_user_ids)
            all_user_weights = np.concatenate(all_user_weights)
            weights = pd.DataFrame({'user_id': all_user_ids, 'ops_weight': all_user_weights})
            return county_weighted_avgs, weights
        else:
            return county_weighted_avgs


def train_model(model, X, y, epochs=100, lr=0.1, patience=3, decays=1, random_state=None, loss_type='pearsonr', metric_type='pearsonr',
                keep_frac=-1):
    optimizer = optim.Adam(model.parameters(), lr=lr)

    X_train_all, X_test, y_train_all, y_test = train_test_split(X, y, test_size=0.2, random_state=random_state)
    print(f'Train size = {len(y_train_all)}, Test size = {len(y_test)}')

    train_rs, train_mses = [], []
    test_rs, test_mses = [], []
    best_metric = -np.inf
    no_metric_change = 0
    total_decays = 0
    best_state = deepcopy(model.state_dict())
    for e in range(epochs):
        print(f'=== Epoch {e + 1} ===')
        if keep_frac > 0:
            X_train, y_train = shuffle(X_train_all, y_train_all, n_samples=int(keep_frac*len(y_train_all)))
            print(f'Train size = {len(y_train)}, Test size = {len(y_test)}')
        else:
            X_train, y_train = shuffle(X_train_all, y_train_all)

        (train_r, train_p), train_r2, train_mse = train(model, X_train, y_train, optimizer, loss_type)
        (test_r, test_p), test_r2, test_mse = test(model, X_test, y_test)
        print(f'Train | r = {train_r:.4f} (p = {train_p:.3e}), MSE = {train_mse:.4f}')
        print(f'Test  | r = {test_r:.4f} (p = {test_p:.3e}), MSE = {test_mse:.4f}')

        if metric_type == 'pearsonr':
            test_metric = test_r
        elif metric_type == 'mse':
            test_metric = -test_mse
        else:
            raise ValueError(f'invalid test metric: {metric_type}')

        if test_metric > best_metric:
            best_metric = test_metric
            no_metric_change = 0
            best_state = deepcopy(model.state_dict())
        else:
            no_metric_change += 1
            if no_metric_change > patience:
                total_decays += 1
                if total_decays > decays:
                    print('early stopping')
                    break
                print('decaying learning rate')
                model.load_state_dict(best_state)
                for g in optimizer.param_groups:
                    g['lr'] /= 10
                no_metric_change = 0

        train_rs.append(train_r)
        train_mses.append(train_mse)
        test_rs.append(test_r)
        test_mses.append(test_mse)

    model.load_state_dict(best_state)
    return train_rs, train_mses, test_rs, test_mses


