def weighted_calculation(data, n=-1, frac=0, weight_col=None):
    rs, mses = [], []
    for person_feat, spatial_var in zip(person_outcomes, spatial_outcomes):
        if weight_col:
            weighted_users = data[person_feat] * data[weight_col]
            if n > 0:
                aggregated = weighted_users.groupby(spatial_group).sample(n).groupby(spatial_group).mean()
            elif frac > 0:
              aggregated = weighted_users.groupby(spatial_group).sample(frac=frac).groupby(spatial_group).mean()
            else:
                aggregated = weighted_users.groupby(spatial_group).mean()
            aggregated.name = person_feat
        else: # weights all = 1
            if n > 0:
                aggregated = data[person_feat].groupby(spatial_group).sample(n).groupby(spatial_group).mean()
            elif frac > 0:
                aggregated = data[person_feat].groupby(spatial_group).sample(frac=frac).groupby(spatial_group).mean()
            else:
                aggregated = data[person_feat].groupby(spatial_group).mean()
        gallup_score = COUNTY_OUTCOMES_DF[spatial_var]
        merged = pd.concat([aggregated, gallup_score], join='inner', axis=1)
        r = pearsonr(merged[person_feat], merged[spatial_var])[0]
        mse = mean_squared_error(merged[person_feat], merged[spatial_var])
        #print(f'{person_feat}: r = {r:.4f}, mse = {mse:.4f}')
        rs.append(r)
        mses.append(mse)
    return rs, mses

