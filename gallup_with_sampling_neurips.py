"""## Function to one-hot encode each user socio-demographics"""

def get_user_demographic_embeddings(user_demographics, twitter_bins):
    dem_binned = age_binned = pd.cut(user_demographics, bins=twitter_bins, right=False, labels=False)
    one_hot_encoding = np.eye(len(twitter_bins) - 1)[dem_binned]
    return one_hot_encoding

def percentage_multi_hot_encoding(percentages):
    # percentages: [age_bins (11), inc_bins (10), gen_bins (2), edu_bins (2)]
    # encoding: (|age_bins| + |inc_bins| + |gen_bins| + |edu_bins|)
    return torch.concat(percentages, axis=0)

def embeddings_multi_hot_encoding(embeddings):
    # percentages: [age_bins (u, 11), inc_bins (u, 10), gen_bins (u, 2), edu_bins (u, 2)]
    # encoding: (u, |age_bins| + |inc_bins| + |gen_bins| + |edu_bins|)
    return np.concatenate(embeddings, axis=1)

def percentage_one_hot_encoding(percentages):
    # percentages: [age_bins (11), inc_bins (10), gen_bins (2), edu_bins (2)]
    # encoding: (|age_bins| * |inc_bins| * |gen_bins| * |edu_bins|)
    encoding = percentages[0]
    for p in percentages[1:]:
        encoding = torch.outer(encoding, p).ravel()
    return encoding

@jit(nopython=True)
def embeddings_one_hot_encoding(embeddings):
    # percentages: [age_bins (u, 11), inc_bins (u, 10), gen_bins (u, 2), edu_bins (u, 2)]
    # encoding: (u, |age_bins| * |inc_bins| * |gen_bins| * |edu_bins|)
    encoding = embeddings[0]
    num_users = len(encoding)
    for e in embeddings[1:]:
        new_dim = len(encoding[0]) * len(e[0])
        new_encoding = np.zeros((num_users, new_dim))
        for i in range(num_users):
            x = encoding[i]
            y = e[i]
            cross = np.outer(x, y).ravel()
            new_encoding[i] = cross
        encoding = new_encoding
        # encoding = [np.outer(encoding[i], e[i]).ravel() for i in range(num_users)]
    return encoding

